package com.nit.string_literal;

public class StringLiteralTest2 {
	
	public static void main(String[] args)
	{
		String x= "B-61 Hyderabad";
		System.out.println(x);
		
		String y= "123";
		System.out.println(y);
		
		String z= "67.90";
		System.out.println(z);
		
		String p = "A";
		System.out.println(p);
	}
}

/*
OUTPUT

B-61 Hyderabad
123
67.90
A
*/